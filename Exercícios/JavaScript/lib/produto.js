function calculaProduto(a, b) {
	// Declaração de variáveis
  	var produto;

  	// Processamento de dados
  	produto = a * b;
  	
  	return produto;
}

let c1 = calculaProduto(10,3);
console.log(c1);

let c2 = calculaProduto(9,3);
console.log(c2);

let c3 = calculaProduto(9,2);
console.log(c3);

let c4 = calculaProduto(7,5);
console.log(c4);

let c5 = calculaProduto(0,5);
console.log(c5);